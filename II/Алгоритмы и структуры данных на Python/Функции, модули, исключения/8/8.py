# В функцию передаются 3 кортежа - координаты x,y точек.
# Необходимо найти площадь образованного этими точками треугольника.

def Function(*args):
    res= (1/2)*(((args[1][0]-args[0][0])*(args[2][1]-args[0][1]))-((args[2][0]-args[0][0])*(args[1][1]-args[0][1])))
    if res<0:
        res*=-1
    print(res)
    return res
